1.Question 1
A computer program is said to learn from experience E with respect to some task T and some performance measure P if its performance on 
T, as measured by P, improves with experience E.Suppose we feed a learning algorithm a lot of historical weather data, and have it 
learn to predict weather. What would be a reasonable choice for P?

(a) The process of the algorithm examining a large amount of historical weather data.
(b) The probability of it correctly predicting a future date's weather.
(c) None of these.
(d) The weather prediction task.

Ans :- The probability of it correctly predicting a future date's weather.

2.Question 2
Suppose you are working on weather prediction, and your weather station makes one of three predictions for each day's weather: Sunny, 
Cloudy or Rainy. You'd like to use a learning algorithm to predict tomorrow's weather. Would you treat this as a classification or 
a regression problem?

(a) Regression
(b) Classification

Ans :- Classification

3.Question 3
Suppose you are working on stock market prediction. You would like to predict whether or not a certain company will declare bankruptcy
within the next 7 days (by training on data of similar companies that had previously been at risk of bankruptcy). Would you treat this
as a classification or a regression problem?

(a) Regression
(b) Classification

Ans :- Classification

